public class Question {

    float fl;
    char ch;
    double dou;
    int in;
    String st;


    public static void main(String[] args) {
        Question question = new Question();
        System.out.println("float: " + question.fl);
        System.out.println("char: " + question.ch);
        System.out.println("double: " + question.dou);
        System.out.println("int: " + question.in);
        System.out.println("String: " + question.st);
    }

}